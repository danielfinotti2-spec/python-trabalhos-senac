# idade = variavel
# possui_convite = variavel verdadeira 

idade = input("Digite Sua Idade:")
possui_convite = True

# if = se 
# idade = variavel 
# >= = maior igual
# 18 and possui_convite == True = 18 for maior e se possui convite dar 
# Verdadeiro: Entrada Autorizada. Falso: Entrada negada.

if idade >= 18 and possui_convite == True:
    print("Entrada Autorizada.")
else:
    print("Entrada Negada.")