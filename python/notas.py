nota = float(input("Digite sua Nota!:"))
if nota >= 9:
    print("Excelente!")
elif nota >= 7:
    print("Aprovado!")
elif nota >= 5:
    print("Recuperaçao!")
elif nota <= 4:
    print("Reprovado!")

